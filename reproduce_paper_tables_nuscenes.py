#!/usr/bin/env python3
"""
Collect paper-table reproduction artifacts for the nuScenes-only project.

This script is intentionally lightweight: it does not train a model and does not
run inference. It collects existing artifacts from the root AutoVLA repository and
exports side-by-side Markdown/JSON tables. Heavy tables such as runtime profiling
and full nuScenes evaluation should be generated by their dedicated SLURM scripts
first, then this script collects the outputs.

The function names follow the final-project expectation: one function per paper
table. Tables outside the nuScenes-only scope are still represented with a clear
scope note instead of silently disappearing.
"""

from __future__ import annotations

import json
