REPRO_DIR = Path(__file__).resolve().parent
ROOT_DIR = REPRO_DIR.parent
OUT_DIR = REPRO_DIR / "evaluation_results"


def fmt(value, digits=4):
    if value is None:
        return "TBD"
    if isinstance(value, float):
        if math.isnan(value) or math.isinf(value):
            return "nan"
        return f"{value:.{digits}f}"
    return str(value)


def load_json(path: Path):
    if not path.exists():
        return None
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def markdown_table(headers, rows):
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join(["---"] * len(headers)) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(lines)


def avg3(a, b, c):
    if a is None or b is None or c is None:
        return None
    return (a + b + c) / 3.0


def parse_planning_table(path: Path):
    if not path.exists():
        return {}
    text = path.read_text(encoding="utf-8", errors="ignore")
    rows = re.findall(
        r"\|\s*(obj_col|obj_box_col|L2)\s*\|\s*"
        r"([0-9.]+|nan)\s*\|\s*([0-9.]+|nan)\s*\|\s*([0-9.]+|nan)\s*\|\s*"
        r"([0-9.]+|nan)\s*\|\s*([0-9.]+|nan)\s*\|\s*([0-9.]+|nan)\s*\|",
        text,
    )
    if len(rows) < 6:
        return {}

    final_rows = rows[-6:]
    horizons = ["0.5s", "1.0s", "1.5s", "2.0s", "2.5s", "3.0s"]
    protocols = {"stp3": {}, "uniad": {}}
    for protocol, protocol_rows in [("stp3", final_rows[:3]), ("uniad", final_rows[3:])]:
        for row in protocol_rows:
            metric = row[0]
            protocols[protocol][metric] = {
                horizon: float(value) if value != "nan" else float("nan")
                for horizon, value in zip(horizons, row[1:])
            }
    return protocols


def load_planning_metrics():
    """Load Table S2 metrics from the most stable available local artifact."""
    candidates = [
        ROOT_DIR / "logs" / "12_evaluate.out",
        ROOT_DIR / "planning_table.txt",
        ROOT_DIR / "evaluation_results_20260501_222241" / "planning_table.txt",
    ]
    for path in candidates:
        local = parse_planning_table(path)
        if local:
            return local, path
    return {}, None


def count_json_files(path: Path) -> int:
    return len(list(path.glob("*.json"))) if path.exists() else 0


def unsupported_table(table_id, title, reason):
    return {
        "table_id": table_id,
        "title": title,
        "status": "not_reproduced_in_nuscenes_only_scope",
        "reason": reason,
        "rows": [],
    }


