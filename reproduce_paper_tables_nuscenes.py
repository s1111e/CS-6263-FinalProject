def dataset_context():
    train = count_json_files(ROOT_DIR / "data" / "nuscenes_processed" / "train")
    val = count_json_files(ROOT_DIR / "data" / "nuscenes_processed" / "val")
    return {
        "train_samples": train,
        "val_samples": val,
        "total_samples": train + val,
        "scope": "nuScenes-only",
    }


def build_markdown(results):
    lines = [
        "# Paper Table Reproduction Status - nuScenes Only",
        "",
        "This file is generated by `reproduce_paper_tables_nuscenes.py`.",
        "It prioritizes the original paper tables and shows which ones are reproduced, partially reproduced, or out of scope for the current nuScenes-only project.",
        "",
        "## Dataset Context",
        "",
        markdown_table(
            ["Split", "Samples"],
            [
                ["Train", results["dataset_context"]["train_samples"]],
                ["Validation", results["dataset_context"]["val_samples"]],
                ["Total", results["dataset_context"]["total_samples"]],
            ],
        ),
        "",
    ]

    for key in ["table_1", "table_2", "table_3", "table_4", "table_5", "table_6", "table_s2"]:
        table = results[key]
        lines += [f"## {table['table_id']}: {table['title']}", "", f"Status: `{table['status']}`", ""]
        if table["table_id"] == "Table 2":
            local_summary = (table.get("local") or {}).get("summary", {})
            local_fast = local_summary.get("fast_thinking", {})
            local_slow = local_summary.get("slow_thinking", {})
            local_ratio = local_summary.get("ratio", {})
            lines += [
                markdown_table(
                    ["Mode", "Paper min", "Paper max", "Paper avg", "Local min", "Local max", "Local avg", "Local ratio"],
                    [
                        [
                            "Fast",
                            table["paper"]["fast_min_s"],
                            table["paper"]["fast_max_s"],
                            table["paper"]["fast_avg_s"],
                            fmt(local_fast.get("min"), 3),
                            fmt(local_fast.get("max"), 3),
                            fmt(local_fast.get("mean"), 3),
                            "1.0x" if local_fast else "TBD",
                        ],
                        [
                            "Slow",
                            table["paper"]["slow_min_s"],
                            table["paper"]["slow_max_s"],
                            table["paper"]["slow_avg_s"],
                            fmt(local_slow.get("min"), 3),
                            fmt(local_slow.get("max"), 3),
                            fmt(local_slow.get("mean"), 3),
                            f"{local_ratio.get('mean'):.1f}x" if local_ratio.get("mean") is not None else "TBD",
                        ],
                    ],
                ),
                "",
                f"Local artifact: `{table['artifact']}`",
                "",
                f"Generate with: `{table['how_to_generate']}`",
                "",
            ]
        elif table["table_id"] == "Table 4":
            rows = []
            for k in ["256", "1024", "2048", "4096"]:
                local = table["local_kdisk"].get(k, {})
                rows.append(
                    [
                        k,
                        table["paper"][k]["kdisk_ade"],
                        fmt(local.get("ADE")),
                        table["paper"][k]["kdisk_fde"],
                        fmt(local.get("FDE")),
                        table["paper"][k]["mc"],
                        fmt(local.get("MC"), 2),
                        table["paper"][k]["cu"],
                        fmt(local.get("CU"), 2),
                    ]
                )
            lines += [
                markdown_table(
                    ["K", "Paper ADE", "Local ADE", "Paper FDE", "Local FDE", "Paper MC", "Local MC", "Paper CU", "Local CU"],
                    rows,
                ),
                "",
                table["note"],
                "",
            ]
        elif table["table_id"] == "Table S2":
            local = table.get("local", {})
            if local:
                paper_headers = [
                    "Method",
                    "ST-P3 L2@1s", "ST-P3 L2@2s", "ST-P3 L2@3s", "ST-P3 Avg L2",
                    "ST-P3 Coll@1s", "ST-P3 Coll@2s", "ST-P3 Coll@3s", "ST-P3 Avg Coll",
                    "UniAD L2@1s", "UniAD L2@2s", "UniAD L2@3s", "UniAD Avg L2",
                    "UniAD Coll@1s", "UniAD Coll@2s", "UniAD Coll@3s", "UniAD Avg Coll",
                ]

                def paper_row(label, key):
                    p = table["paper"][key]
                    return [
                        label,
                        p["stp3_l2_1s"], p["stp3_l2_2s"], p["stp3_l2_3s"], p["stp3_avg_l2"],
                        p["stp3_coll_1s"], p["stp3_coll_2s"], p["stp3_coll_3s"], p["stp3_avg_coll"],
                        p["uniad_l2_1s"], p["uniad_l2_2s"], p["uniad_l2_3s"], p["uniad_avg_l2"],
                        p["uniad_coll_1s"], p["uniad_coll_2s"], p["uniad_coll_3s"], p["uniad_avg_coll"],
                    ]

                stp3_l2 = local["stp3"]["L2"]
                stp3_col = local["stp3"]["obj_col"]
                uniad_l2 = local["uniad"]["L2"]
                uniad_col = local["uniad"]["obj_col"]
                rows = [
                    paper_row("AutoVLA action-only (paper)", "action_only"),
                    paper_row("AutoVLA w/ CoT (paper)", "with_cot"),
                    [
                        "AutoVLA nuScenes-only reproduction (ours)",
                        fmt(stp3_l2["1.0s"]), fmt(stp3_l2["2.0s"]), fmt(stp3_l2["3.0s"]),
                        fmt(avg3(stp3_l2["1.0s"], stp3_l2["2.0s"], stp3_l2["3.0s"])),
                        fmt(stp3_col["1.0s"]), fmt(stp3_col["2.0s"]), fmt(stp3_col["3.0s"]),
                        fmt(avg3(stp3_col["1.0s"], stp3_col["2.0s"], stp3_col["3.0s"])),
                        fmt(uniad_l2["1.0s"]), fmt(uniad_l2["2.0s"]), fmt(uniad_l2["3.0s"]),
                        fmt(avg3(uniad_l2["1.0s"], uniad_l2["2.0s"], uniad_l2["3.0s"])),
                        fmt(uniad_col["1.0s"]), fmt(uniad_col["2.0s"]), fmt(uniad_col["3.0s"]),
                        fmt(avg3(uniad_col["1.0s"], uniad_col["2.0s"], uniad_col["3.0s"])),
                    ],
                ]
                lines += [
                    markdown_table(paper_headers, rows),
                    "",
                    f"Local artifact: `{table['artifact']}`",
                    "",
                    table["note"],
                    "",
                    "Collision naming note: the local evaluator reports `obj_col` and `obj_box_col`; the paper reports collision rate. Keep them side by side until metric alignment is finalized.",
                    "",
                    f"Generate with: `{table['how_to_generate']}`",
                    "",
                ]
            else:
                lines += ["Local planning metrics were not found.", ""]
        else:
            lines += [table["reason"], ""]
    return "\n".join(lines)


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    results = {
        "dataset_context": dataset_context(),
        "table_1": reproduce_table_1(),
        "table_2": reproduce_table_2(),
        "table_3": reproduce_table_3(),
        "table_4": reproduce_table_4(),
        "table_5": reproduce_table_5(),
        "table_6": reproduce_table_6(),
        "table_s2": reproduce_table_s2(),
    }
    json_path = OUT_DIR / "paper_tables_nuscenes.json"
    md_path = OUT_DIR / "paper_tables_nuscenes.md"
    json_path.write_text(json.dumps(results, indent=2), encoding="utf-8")
    md_path.write_text(build_markdown(results), encoding="utf-8")
    print(f"Wrote {json_path}")
    print(f"Wrote {md_path}")


if __name__ == "__main__":
    main()
